public class NestedObject {

    private int fieldInt;

    private String fieldString;

    public void setFieldInt(int i) {
        fieldInt = i;
    }

    public void setFieldString(String s) {
        fieldString = s;
    }

    public int getFieldInt() {
        return fieldInt;
    }

    public String getFieldString() {
        return fieldString;
    }
}
